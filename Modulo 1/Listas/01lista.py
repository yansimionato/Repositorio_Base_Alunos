# Crie uma lista vazia chamada frutas.
# Peça para o usuário digitar o nome de uma fruta e use .append() para adicioná-las à lista.
# Mostre a lista no final.

#RESOLVIDO

frutas = [] # criamos a lista vazia
fruta = input("Digite o nome de uma fruta: ") # solicitamos ao usuário a informação
frutas.append(fruta) # adicionamos a fruta informada na lista
print("Lista de frutas:", frutas) # apresentamos a lista