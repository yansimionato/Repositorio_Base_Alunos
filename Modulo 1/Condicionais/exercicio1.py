# Peça dois números ao usuário. Se forem diferentes, exiba "Os números são diferentes", caso contrário, exiba "Os números são iguais".

numero1 =float(input("Digite um numero: "))
numero2 = float(input("Digite outro numero: "))
if numero1 != numero2:
    print(f"O número são diferentes.  ")
else:
    print(f"O número são iguais.  ") 