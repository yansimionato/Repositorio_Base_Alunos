# Condicional Simples

# Para condições em Python utilizamos a palavra if que signfica 'se' em português.
# Com a condicional, você consegue executar um conjunto 
# de código APENAS se o resultado da escpressão for "Verdadeiro" (True)

idade = int(input("Qual a sua idade? "))
if idade >= 18:
    print("Maior de idade")
else:
    print("Menor de idade")